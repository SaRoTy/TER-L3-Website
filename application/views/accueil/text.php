		<h2>Quel est cette application ?</h2>
		</br>
			<p> La mission du TER est de créé une application qui permet a un étudiant de pouvoir affiché son emploi du temps pour tous les jours qu'il
souhaite.De plus l'étudiant pourra choisir les UEs qu'il veux suivre et son emploi du temps s'affichera en fonctions. Pour cela, nous devons
créer un site web qui permettra au responsable de spécialité d'éditer un fichier de configuration pour les étudiants de sa spécialité. Ce
fichier sera ensuite télécharger par l'étudiant.</p>
